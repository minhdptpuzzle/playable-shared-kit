function align4(value) {
  return (value + 3) & ~3;
}

function paddedBuffer(buffer) {
  const aligned = align4(buffer.length);
  if (aligned === buffer.length) return buffer;
  return Buffer.concat([buffer, Buffer.alloc(aligned - buffer.length)]);
}

function floatBuffer(values) {
  const buffer = Buffer.alloc(values.length * 4);
  values.forEach((value, index) => buffer.writeFloatLE(Number(value) || 0, index * 4));
  return paddedBuffer(buffer);
}

function indexBuffer(values) {
  const useUint32 = values.some((value) => value > 65535);
  const buffer = Buffer.alloc(values.length * (useUint32 ? 4 : 2));
  values.forEach((value, index) => {
    if (useUint32) buffer.writeUInt32LE(Number(value) || 0, index * 4);
    else buffer.writeUInt16LE(Number(value) || 0, index * 2);
  });
  return { buffer: paddedBuffer(buffer), componentType: useUint32 ? 5125 : 5123 };
}

function minMaxVec3(values) {
  const min = [Infinity, Infinity, Infinity];
  const max = [-Infinity, -Infinity, -Infinity];
  for (const value of values) {
    for (let i = 0; i < 3; i += 1) {
      const n = Number(value[i]) || 0;
      min[i] = Math.min(min[i], n);
      max[i] = Math.max(max[i], n);
    }
  }
  return { min, max };
}

function writeFileIfChanged(file, content, encoding = null) {
  if (fs.existsSync(file)) {
    const current = encoding ? fs.readFileSync(file, encoding) : fs.readFileSync(file);
    if (encoding ? current === content : Buffer.compare(current, content) === 0) return false;
  }
  fs.writeFileSync(file, content, encoding || undefined);
  return true;
}

function writeUnityMeshAssetAsGltf(mesh, destFile) {
  const name = sanitizeFbxName(mesh.meshName);
  const binFile = `${path.basename(destFile, path.extname(destFile))}.bin`;
  const binPath = path.join(path.dirname(destFile), binFile);
  const positions = mesh.positions.flat();
  const normals = mesh.normals.flat();
  const uvs = mesh.uvs.flat();
  const indexData = indexBuffer(mesh.indices);
  const positionData = floatBuffer(positions);
  const normalData = floatBuffer(normals);
  const uvData = floatBuffer(uvs);

  const chunks = [];
  const pushView = (buffer, target) => {
    const byteOffset = chunks.reduce((sum, chunk) => sum + chunk.buffer.length, 0);
    chunks.push({ buffer, target, byteOffset });
    return chunks.length - 1;
  };

  const indexView = pushView(indexData.buffer, 34963);
  const positionView = pushView(positionData, 34962);
  const normalView = pushView(normalData, 34962);
  const uvView = pushView(uvData, 34962);
  const combined = Buffer.concat(chunks.map((chunk) => chunk.buffer));
  writeFileIfChanged(binPath, combined);

  const bounds = minMaxVec3(mesh.positions);
  const gltf = {
    asset: {
      version: '2.0',
      generator: 'unity-cocos-port',
    },
    buffers: [{
      uri: binFile,
      byteLength: combined.length,
    }],
    bufferViews: chunks.map((chunk) => ({
      buffer: 0,
      byteOffset: chunk.byteOffset,
      byteLength: chunk.buffer.length,
      target: chunk.target,
    })),
    accessors: [
      {
        bufferView: indexView,
        byteOffset: 0,
        componentType: indexData.componentType,
        count: mesh.indices.length,
        type: 'SCALAR',
      },
      {
        bufferView: positionView,
        byteOffset: 0,
        componentType: 5126,
        count: mesh.positions.length,
        type: 'VEC3',
        min: bounds.min,
        max: bounds.max,
      },
      {
        bufferView: normalView,
        byteOffset: 0,
        componentType: 5126,
        count: mesh.normals.length,
        type: 'VEC3',
      },
      {
        bufferView: uvView,
        byteOffset: 0,
        componentType: 5126,
        count: mesh.uvs.length,
        type: 'VEC2',
      },
    ],
    meshes: [{
      name,
      primitives: [{
        attributes: {
          POSITION: 1,
          NORMAL: 2,
          TEXCOORD_0: 3,
        },
        indices: 0,
        mode: 4,
      }],
    }],
    nodes: [{
      name,
      mesh: 0,
    }],
    scenes: [{
      nodes: [0],
    }],
    scene: 0,
  };
  writeFileIfChanged(destFile, `${JSON.stringify(gltf, null, 2)}\n`, 'utf8');
}

function exportUnityMeshAssetToFbx(meshFile, destFile) {
  const mesh = parseUnityMeshAsset(meshFile);
  if (!mesh) return null;
  fs.mkdirSync(path.dirname(destFile), { recursive: true });
  writeUnityMeshAssetAsFbx(mesh, destFile);
  return {
    destFile,
    meshName: mesh.meshName,
    vertexCount: mesh.positions.length,
    indexCount: mesh.indices.length,
  };
}

function exportUnityMeshAssetToGltf(meshFile, destFile) {
  const mesh = parseUnityMeshAsset(meshFile);
  if (!mesh) return null;
  fs.mkdirSync(path.dirname(destFile), { recursive: true });
  writeUnityMeshAssetAsGltf(mesh, destFile);
  return {
    destFile,
    meshName: mesh.meshName,
    vertexCount: mesh.positions.length,
    indexCount: mesh.indices.length,
  };
}

module.exports = {
  exportUnityMeshAssetToFbx,
  exportUnityMeshAssetToGltf,
  parseUnityMeshAsset,
};
